export function getMunicipalities(county) {
  return NY_MUNICIPALITIES[county] || [];
}

export function getMunicipalityBySWIS(swis) {
  for (const [county, towns] of Object.entries(NY_MUNICIPALITIES)) {
    const town = towns.find(t => t.swis === swis);
    if (town) return { ...town, county };
  }
  return null;
}

