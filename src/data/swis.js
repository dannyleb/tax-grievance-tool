// Grievance Day — 4th Tuesday of May (most NY jurisdictions)
export function getGrievanceDay(year = new Date().getFullYear()) {
  // Find 4th Tuesday of May
  const may1 = new Date(year, 4, 1); // May 1
  const dayOfWeek = may1.getDay(); // 0=Sun, 2=Tue
  const firstTuesday = dayOfWeek <= 2 ? 2 - dayOfWeek + 1 : 9 - dayOfWeek + 2;
  const fourthTuesday = firstTuesday + 21;
  return new Date(year, 4, fourthTuesday);
}
